import { spawn } from 'node:child_process';
import { createServer } from 'node:net';
import http from 'node:http';
import path from 'node:path';
import { chromium } from 'playwright-core';
const sleep=ms=>new Promise(r=>setTimeout(r,ms));
const freePort=()=>new Promise((res,rej)=>{const s=createServer();s.listen(0,'127.0.0.1',()=>{const p=s.address().port;s.close(()=>res(p));});s.on('error',rej);});
const waitHttp=u=>new Promise((res,rej)=>{const t0=Date.now();(function poll(){const rq=http.get(u,r=>{r.resume();res();});rq.on('error',()=>{if(Date.now()-t0>15000)rej(new Error('no server'));else setTimeout(poll,250);});})();});
(async()=>{ const port=await freePort();
  const server=spawn(process.execPath,['mp-server.js'],{env:{...process.env,MP_PORT:String(port),MP_DISC:String(port+1)},stdio:'ignore'});
  try{ await waitHttp('http://127.0.0.1:'+port+'/index.html');
    const browser=await chromium.launch({executablePath:'C:\\Program Files\\Google\\Chrome\\Application\\chrome.exe',headless:true,args:['--enable-gpu','--ignore-gpu-blocklist','--use-angle=d3d11','--mute-audio']});
    const page=await (await browser.newContext({viewport:{width:1280,height:720}})).newPage();
    await page.goto('http://127.0.0.1:'+port+'/index.html?debug=1&t=630',{waitUntil:'load',timeout:90000});
    await page.waitForFunction("(()=>{try{return window.__hc&&__hc.st().started===true;}catch(e){return false;}})()",{timeout:90000});
    await sleep(1500);
    await page.evaluate("(()=>{ __hc.freeze(true,true); __hc.studio(true); __hc.frame({dist:10, ang:0, state:'HUNT', crawl:1, adv:8, committed:true, aim:2.4}); __hc.spin(115); __hc.set({dist:5}); })()");
    await sleep(1200);
    await page.screenshot({path:'bench/results/torso-rise.png'});
    const p=await page.evaluate("__hc.pose()"); console.log(JSON.stringify(p));
    await browser.close(); process.exit(0);
  } finally { try{server.kill();}catch(e){} } })().catch(e=>{console.error(e);process.exit(1);});
